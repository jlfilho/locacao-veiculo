package uea.jlfilho.locadora.entities.primitive;

public class Localidade {
	Double s;
	Double w;
	
	public Localidade () {
		
	}

	public Localidade(Double s, Double w) {
		super();
		this.s = s;
		this.w = w;
	}

	public Double getS() {
		return s;
	}

	public void setS(Double s) {
		this.s = s;
	}

	public Double getW() {
		return w;
	}

	public void setW(Double w) {
		this.w = w;
	}
	
	
}

